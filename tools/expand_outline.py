# 建立章节→事件链映射
chapter_to_chain = {}
for start, end, desc in event_chains:
    for ch in range(start, end + 1):
        chapter_to_chain[ch] = (start, end, desc)

# 技能解锁映射
skill_unlocks = {
    2: "鼓楼视野（觉醒）",
    5: "铜鼓震步（首次实战）",
    10: "榫卯传球（首次亮相）",
    12: "踩堂定位球（解锁）",
    14: "萨玛护体（解锁）/侗魂爆发（首次触发）",
    20: "风雨桥连携（解锁）/银饰眩光（解锁）/大歌共振（解锁）",
    30: "鼓楼阵·十一榫（解锁）/萨玛降世（首次触发）",
    50: "世界之眼（解锁中）",
}

def get_arc(ch):
    for arc, s, e in arc_sections:
        if s <= ch <= e:
            return arc
    return "未知阶段"

def generate_entry(ch):
    """为缺失章节生成 OUTLINE 条目"""
    start, end, desc = chapter_to_chain.get(ch, (ch, ch, "剧情推进"))
    arc = get_arc(ch)
    
    # 在事件链中的位置
    total = end - start + 1
    idx = ch - start + 1
    
    # 根据位置生成递进描述
    phase_words = ["开局", "推进", "深入", "转折", "高潮", "收尾"]
    phase = phase_words[min(idx - 1, len(phase_words) - 1)] if total <= 6 else \
            phase_words[min((idx - 1) * len(phase_words) // total, len(phase_words) - 1)]
    
    core = f"【{phase}】{desc}（第{idx}/{total}章）"
    
    # 其他字段
    face_slap_target = ""
    face_slap_method = ""
    husband = ""
    hook = ""
    
    if "打脸" in desc or "嘲讽" in desc or "逆转" in desc or "绝杀" in desc:
        face_slap_target = "对手/质疑者"
        face_slap_method = "用实力回应质疑"
    if "主角" in desc:
        husband = "主角在困境中坚持信念，带领球队前进"
    if idx == total:
        hook = "事件链收尾，为下一阶段埋下伏笔"
    elif "绝杀" in desc or "逆转" in desc or "高潮" in desc:
        hook = "关键时刻的转折，悬念升级"
    else:
        hook = "剧情持续推进，紧张感逐步累积"
    
    # 情绪比例
    emotions = arc_emotions.get(arc, [])
    emotion = emotions[ch - 1] if ch <= len(emotions) else "3:3:4"
    
    # 技能
    skill = skill_unlocks.get(ch, "")
    
    return {
        "chapter": ch,
        "arc": arc,
        "core_event": core,
        "face_slap_target": face_slap_target,
        "face_slap_method": face_slap_method,
        "husband_moment": husband,
        "chapter_hook": hook,
        "emotion_ratio": emotion,
        "skill_unlocked": skill,
    }


def format_entry(e):
    """将字典格式化为 Python 代码字符串"""
    lines = [
        '    {',
        f'        "chapter": {e["chapter"]},',
        f'        "arc": "{e["arc"]}",',
        f'        "core_event": "{e["core_event"]}",',
        f'        "face_slap_target": "{e["face_slap_target"]}",',
        f'        "face_slap_method": "{e["face_slap_method"]}",',
        f'        "husband_moment": "{e["husband_moment"]}",',
        f'        "chapter_hook": "{e["chapter_hook"]}",',
        f'        "emotion_ratio": "{e["emotion_ratio"]}",',
        f'        "skill_unlocked": "{e["skill_unlocked"]}",',
        '    },',
    ]
    return "\n".join(lines)


# ── 2. 读取现有 book_data.py，提取已有条目 ──
py_text = PY_PATH.read_text(encoding="utf-8")

# 找到 OUTLINE 列表的范围
outline_start = py_text.find("OUTLINE = [")
outline_end = py_text.find("]\n\n# ═══════════════════════════════════════", outline_start)
if outline_end == -1:
    outline_end = py_text.find("]\n\n# 2.", outline_start)

# 提取现有的 OUTLINE 条目（用 exec 解析）
existing = {}
outline_code = py_text[outline_start:outline_end + 1]
local_ns = {}
exec(outline_code, local_ns)
for item in local_ns["OUTLINE"]:
    existing[item["chapter"]] = item

print(f"现有条目：{len(existing)} 章")

# ── 3. 生成完整的200章 ──
full_outline = []
for ch in range(1, 201):
    if ch in existing:
        full_outline.append(existing[ch])
    else:
        full_outline.append(generate_entry(ch))

print(f"生成总条目：{len(full_outline)} 章")

# ── 4. 组装新的 OUTLINE 代码 ──
sections = []
current_arc = ""
for e in full_outline:
    if e["arc"] != current_arc:
        current_arc = e["arc"]
        # 找到卷的起止章节
        for arc_name, s, end_ch in arc_sections:
            if arc_name == current_arc:
                sections.append(f'    # ── {current_arc}（第{s}-{end_ch}章）──')
                break
    sections.append(format_entry(e))

new_outline_code = "OUTLINE = [\n" + "\n".join(sections) + "\n]"

# ── 5. 替换并写入 ──
new_py_text = py_text[:outline_start] + new_outline_code + PY_PATH.read_text(encoding="utf-8")[outline_end + 1:]
PY_PATH.write_text(new_py_text, encoding="utf-8")

# 验证语法
import py_compile
py_compile.compile(str(PY_PATH), doraise=True)
print("✅ book_data.py 语法验证通过")
print(f"✅ OUTLINE 共 {len(full_outline)} 章")
