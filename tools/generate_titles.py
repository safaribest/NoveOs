def generate_title(ch: int, core_event: str) -> str:
    """获取或生成章节标题。"""
    if ch in TITLES:
        return TITLES[ch]
    # 兜底：基于 core_event 生成
    # 提取关键词
    if "绝杀" in core_event or "逆转" in core_event:
        return "绝杀！"
    if "系统" in core_event and "警告" in core_event:
        return "系统警告"
    if "直播" in core_event:
        return "直播间里的变数"
    if "对手" in core_event:
        return "下一个对手"
    if "训练" in core_event:
        return "训练场的秘密"
    return f"第{ch}章：风云再起"


def main():
    py_text = PY_PATH.read_text(encoding="utf-8")

    # 找到 OUTLINE 列表中的每个条目，插入 title 字段
    # 使用正则替换：在每个 "chapter": N, 后面插入 "title": "...",
    def replacer(m):
        ch = int(m.group(1))
        # 找到这个条目的 core_event 来生成兜底标题
        # 简单方案：直接用预定义映射
        title = TITLES.get(ch, f"第{ch}章")
        return f'        "chapter": {ch},\n        "title": "{title}",'

    new_text = re.sub(r'        "chapter": (\d+),', replacer, py_text)

    # 验证
    import py_compile
    PY_PATH.write_text(new_text, encoding="utf-8")
    py_compile.compile(str(PY_PATH), doraise=True)

    # 统计
    titles = re.findall(r'"title": "([^"]+)"', new_text)
    print(f"已生成 {len(titles)} 个章节标题")
    for ch, t in enumerate(titles[:10], 1):
        print(f"  第{ch}章: {t}")
    print("  ...")
    for ch, t in enumerate(titles[-5:], 196):
        print(f"  第{ch}章: {t}")


if __name__ == "__main__":
    main()
